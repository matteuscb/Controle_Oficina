    private void jTCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTCalcActionPerformed
        try {
            double alt = Double.parseDouble(jTAltura.getText()) / 100;
            double comp = Double.parseDouble(jTComp.getText()) / 100;
            double larg = Double.parseDouble(jTLargura.getText()) / 100;
            double precoEspuma = (((comp * larg) * alt) * 2220);


            if (!jTQnt.getText().equals("")) {
                precoEspuma = (((comp * larg) * alt) * 2220) * Double.parseDouble(jTQnt.getText());
            }
            jTTotal.setText(String.format("R$ %.2f", precoEspuma));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Erro, Preencha todos os campos.\n" + ex);
        }
    }//GEN-LAST:event_jTCalcActionPerformed

