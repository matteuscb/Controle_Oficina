    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JPasswordField jTSenha = new JPasswordField();
        Object[] message = {"Senha: ", jTSenha};
        String senha = "";
        if (JOptionPane.showConfirmDialog(this, message, "SENHA", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            senha = jTSenha.getText();
        }

        if (senha.equals("fiat") || senha.equals("FIAT")) {
            Edicao_Tecido et = new Edicao_Tecido();
            this.dispose();
            painel.add(et);
            et.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Senha incorreta!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

