    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        int pergunta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este fornecedor?");
        if (pergunta == 0) {//clicou em sim  
            try {
                dao = new FornecedorDAO();
                dao.remover(fornecedores.get(Tabela.getSelectedRow()).getCodigo());
                JOptionPane.showMessageDialog(null, "Fornecedor Deletado com Sucesso!");
                limpar();
            } catch (SQLException | ParseException ex) {
                JOptionPane.showMessageDialog(null, "Erro:\n" + ex);
            }
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

