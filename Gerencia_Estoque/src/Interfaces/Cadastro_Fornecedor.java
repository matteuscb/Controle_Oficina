    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
        try {
            dao = new FornecedorDAO();
            fornecedores = dao.listar("%" + jTPesquisar.getText() + "%");

            while (tmCliente.getRowCount() > 0) {
                tmCliente.removeRow(0);
            }

            String endereco;
            for (int i = 0; i < fornecedores.size(); i++) {

                //"Loja", "Vendedor", "Telefone 1", "Telefone 2", "Telefone 3", "Telefone 4", "E-mail", "Endereço", "OBS"});
                tmCliente.addRow(new String[]{null, null, null, null});
                endereco = "Rua: " + fornecedores.get(i).getRua() + "Nº "
                        + fornecedores.get(i).getNumero() + " - "
                        + fornecedores.get(i).getBairro() + ". "
                        + fornecedores.get(i).getCidade() + "/"
                        + fornecedores.get(i).getEstado() + "CEP: "
                        + fornecedores.get(i).getCep();

                tmCliente.setValueAt(fornecedores.get(i).getLoja(), i, 0);
                tmCliente.setValueAt(fornecedores.get(i).getVendedor(), i, 1);
                tmCliente.setValueAt(fornecedores.get(i).getTelefone1(), i, 2);
                tmCliente.setValueAt(fornecedores.get(i).getTelefone2(), i, 3);
                tmCliente.setValueAt(fornecedores.get(i).getTelefone3(), i, 4);
                tmCliente.setValueAt(fornecedores.get(i).getTelefone4(), i, 5);
                tmCliente.setValueAt(fornecedores.get(i).getEmail(), i, 6);
                tmCliente.setValueAt(endereco, i, 7);
                tmCliente.setValueAt(fornecedores.get(i).getObs(), i, 8);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cadastro_Fornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBPesquisarActionPerformed

